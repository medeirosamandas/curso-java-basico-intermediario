package com.amanda.cursojava.aula11;

public class SequenciaEscape {

	public static void main(String[] args) {
		
		// para imprimir a \ barra invertida ou as aspas, é necessário colocar \ antes.
		// \n é utilizado para pular linha

		System.out.print("\"Hello World\"\n");
		System.out.print("1\\4");

	}

}
