package com.amanda.cursojava.aula34.labs;

public class ConversaoDeUnidadesDeArea {
	
	static double metroQuadradoEmPesQuadrados(double metroQuadrado) {
		return metroQuadrado * 10.76;
	}
	
	static double peQuadradoEmCentimetrosQuadrados(double peQuadrado) {
		return peQuadrado * 929;
	}
	static double milhaQuadradaEmAcres(double milhaQuadrada) {
		return milhaQuadrada * 640;
	}
	static double acreEmPesQuadrados(double acre) {
		return acre * 43560;
	}
	

}
