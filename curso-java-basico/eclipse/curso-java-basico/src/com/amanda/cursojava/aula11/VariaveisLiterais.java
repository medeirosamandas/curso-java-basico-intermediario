package com.amanda.cursojava.aula11;

public class VariaveisLiterais {

	public static void main(String[] args) {
		double d1 = 123.4;
		double d2 = 1.234e2;
		float f1 = 123.4f;
		
		System.out.println("Double: " + d1);
		// resultado de d2 é igual d1
		System.out.println("Double notação científica: " + d2);
		System.out.println("Float: " + f1);

	}

}
